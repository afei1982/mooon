// CEpollable

CEpollable::CEpollable()
    :_fd(-1)
    ,_epoll_events(-1)
{
}

CEpollable::~CEpollable()
{
    this->close();
}

void CEpollable::close()
{
    _epoll_events = -1;
    if (_fd != -1)
    {
        before_close();
        close_fd(_fd);
        _fd = -1;
    }        
}

